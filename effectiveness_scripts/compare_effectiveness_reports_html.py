def main() -> int:
    parser = argparse.ArgumentParser(description="Generate a comparative HTML dashboard from GUIA-Madrid-Bench split effectiveness reports.")
    parser.add_argument("reports", type=Path, help="Directory or ZIP containing one subdirectory per technique.")
    parser.add_argument("-o", "--output", type=Path, default=Path("comparative_effectiveness_report.html"), help="Output HTML file.")
    parser.add_argument("--title", default="GUIA-Madrid-Bench comparative effectiveness report")
    args = parser.parse_args()

    reports, temp_dir = load_reports(args.reports)
    try:
        if not reports:
            raise SystemExit(f"No technique report directories found in {args.reports}")
        html_text = build_html(reports, args.title)
        args.output.parent.mkdir(parents=True, exist_ok=True)
        args.output.write_text(html_text, encoding="utf-8")
        print(f"Loaded {len(reports)} techniques: {', '.join(r.name for r in reports)}")
        print(f"Wrote {args.output}")
    finally:
        if temp_dir is not None:
            temp_dir.cleanup()
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
