from __future__ import annotations

from pathlib import Path
from typing import Iterable, Optional, Any
import math
import logging
from contextlib import contextmanager

from rdflib import Graph, RDF, RDFS, OWL, URIRef, Literal, Namespace
from rdflib.term import _toPythonMapping


RDF_FORMAT_BY_SUFFIX = {
    ".ttl": "turtle",
    ".rdf": "xml",
    ".xml": "xml",
    ".nt": "nt",
    ".nq": "nquads",
    ".jsonld": "json-ld",
    ".trig": "trig",
}

_EPSILON = 1e-12



@contextmanager
def suppress_rdflib_datatype_conversion_logs():
    """Silence RDFlib datatype-conversion errors during parsing.

    RDFlib logs messages such as "Failed to convert Literal lexical form to value"
    when a typed literal is syntactically valid Turtle but has an invalid lexical
    value for its XSD datatype, e.g. "1,5"^^xsd:decimal. These messages are
    not Python exceptions and otherwise bypass our extended-report mechanism.
    """
    term_logger = logging.getLogger("rdflib.term")
    old_disabled = term_logger.disabled
    old_level = term_logger.level
    term_logger.disabled = True
    try:
        yield
    finally:
        term_logger.disabled = old_disabled
        term_logger.setLevel(old_level)


def find_invalid_xsd_literals(g: Graph, limit: int = 100) -> list[dict[str, str]]:
    """Return typed literals whose lexical form cannot be converted by RDFlib.

    RDF allows typed literals to be parsed even when their lexical form is not
    valid for the declared datatype. For example, "1,5"^^xsd:decimal is valid
    Turtle syntax but not a valid xsd:decimal lexical form. This helper detects
    these cases so they can be written to extended-report.txt without polluting
    stderr/stdout.
    """
    invalid: list[dict[str, str]] = []
    with suppress_rdflib_datatype_conversion_logs():
        for s, p, o in g.triples((None, None, None)):
            if not isinstance(o, Literal) or o.datatype is None:
                continue
            if o.datatype not in _toPythonMapping:
                continue
            converted = o.toPython()
            if converted is o:
                invalid.append({
                    "subject": str(s),
                    "predicate": str(p),
                    "literal": str(o),
                    "datatype": str(o.datatype),
                })
                if len(invalid) >= limit:
                    break
    return invalid

def clean_float(value: float | None, epsilon: float = _EPSILON) -> float | None:
    """Normalise tiny floating point artefacts generated by cosine/linear algebra operations."""
    if value is None:
        return None
    if isinstance(value, float):
        if math.isnan(value):
            return value
        if abs(value) < epsilon:
            return 0.0
        if abs(value - 1.0) < epsilon:
            return 1.0
    return value



