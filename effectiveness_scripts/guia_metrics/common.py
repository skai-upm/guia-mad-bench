def clean_float_dict(values: dict) -> dict:
    return {k: clean_float(v) if isinstance(v, float) else v for k, v in values.items()}


def safe_divide(numerator: float, denominator: float, default: float | None = None) -> float | None:
    if denominator == 0:
        return default
    return numerator / denominator


def parse_graph(path: str | Path, fmt: str | None = None) -> Graph:
    p = Path(path)
    if fmt is None:
        fmt = RDF_FORMAT_BY_SUFFIX.get(p.suffix.lower(), "turtle")
    g = Graph()
    with suppress_rdflib_datatype_conversion_logs():
        g.parse(str(p), format=fmt)
    return g


def graph_to_triple_set(g: Graph) -> set[tuple[Any, Any, Any]]:
    return set(g.triples((None, None, None)))


def load_ontology_classes(ontology_graph: Graph) -> set:
    return {s for s, _, _ in ontology_graph.triples((None, RDF.type, OWL.Class))}


def load_ontology_properties(ontology_graph: Graph) -> set:
    props = set()
    for cls in (OWL.ObjectProperty, OWL.DatatypeProperty, RDF.Property):
        props |= {s for s, _, _ in ontology_graph.triples((None, RDF.type, cls))}
    return props


def typed_instance_triples(g: Graph, ontology_classes: set) -> set:
    return {(s, p, o) for s, p, o in g.triples((None, RDF.type, None)) if o in ontology_classes}


def property_assertion_triples(g: Graph, ontology_properties: set) -> set:
    return {(s, p, o) for s, p, o in g.triples((None, None, None)) if p in ontology_properties and p != RDF.type}


def datatype_literals(g: Graph) -> set:
    return {o for _, _, o in g.triples((None, None, None)) if isinstance(o, Literal) and o.datatype is not None}


def language_tagged_literals(g: Graph) -> set:
    return {o for _, _, o in g.triples((None, None, None)) if isinstance(o, Literal) and o.language is not None}


def predicate_object_pairs(g: Graph) -> set[tuple[Any, Any]]:
    return {(p, o) for _, p, o in g.triples((None, None, None))}


def literals_by_property(g: Graph) -> dict[Any, list[str]]:
    result: dict[Any, list[str]] = {}
    for _, p, o in g.triples((None, None, None)):
        if isinstance(o, Literal):
            result.setdefault(p, []).append(str(o))
    return result


def mean(values: Iterable[float]) -> float | None:
    vals = [v for v in values if v is not None and not (isinstance(v, float) and math.isnan(v))]
    if not vals:
        return None
    return clean_float(sum(vals) / len(vals))


def std(values: Iterable[float]) -> float | None:
    vals = [v for v in values if v is not None and not (isinstance(v, float) and math.isnan(v))]
    if not vals:
        return None
    m = sum(vals) / len(vals)
    return clean_float(math.sqrt(sum((x - m) ** 2 for x in vals) / len(vals)))


def as_csv_value(value):
    if value is None:
        return ""
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, (list, set, tuple)):
        return "|".join(str(v) for v in value)
    return value


def count_ttl_files(path: str | Path) -> int:
    return len(list(Path(path).rglob("*.ttl")))


def count_triples_in_directory(path: str | Path, logger=None) -> int:
    total = 0
    for ttl in Path(path).rglob("*.ttl"):
        try:
            total += len(parse_graph(ttl, "turtle"))
        except Exception as e:
            if logger:
                logger.log("count_triples_in_directory", "Could not parse generated RDF file while counting triples.",
                           pred_file=str(ttl), exception=e)
    return total


def default_allowed_predicates() -> set:
    """Predicates that are part of RDF/RDFS/OWL metadata and must not be treated as ontology hallucinations.

    The benchmark metric is meant to detect invented domain predicates. The RDF typing predicate is
    required to express class membership and should never be reported as hallucinated.
    """
    return {RDF.type, RDFS.label, RDFS.comment, RDFS.seeAlso, OWL.sameAs}
